// clang-format on
layer_state_t layer_state_set_user(layer_state_t state) { return update_tri_layer_state(state, _LOWER, _RAISE, _ADJUST); }

void keyboard_pre_init_user() {
    setPinOutput(B0);
    writePinHigh(B0);
    setPinOutput(D5);
    writePinHigh(D5);
}

bool led_update_user(led_t usb_led) {
    bool layer1 = IS_LAYER_ON(_LOWER);
    bool layer2 = IS_LAYER_ON(_RAISE);
    writePin(B0, layer1 ? 0 : 1);
    writePin(D5, layer2 ? 0 : 1);
    if (usb_led.caps_lock && !layer1 && !layer2) {
        writePin(B0, 0);
        writePin(D5, 0);
    }

    return false;
}
