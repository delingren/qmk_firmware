#include "delingren.h"

bool led_update_user(led_t usb_led) {
    writePin(C5, !usb_led.caps_lock);
    return false;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    writePin(B7, IS_LAYER_ON_STATE(state, _FL) ? 0 : 1);
    writePin(C6, IS_LAYER_ON_STATE(state, _LL) ? 0 : 1);
    return state;
}


